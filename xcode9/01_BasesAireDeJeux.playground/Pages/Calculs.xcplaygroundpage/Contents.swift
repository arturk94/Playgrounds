//: ## Calculs
//: L’aire de jeux peut être utilisée comme une calculatrice. Tapez le problème de mathématiques que vous souhaitez résoudre, et la barre de résultat vous indiquera la réponse au calcul, autrement dit son _résultat_.
//:
//: Vous pouvez utiliser les signes plus `+` et moins `-` pour effectuer des additions et des soustractions, comme avec une calculatrice :
34 + 56 + 230

1000 - 300

//: Pour multiplier, utilisez l’astérisque `*` :
3 * 100
//: Pour diviser, utilisez la barre oblique `/` :
24 / 8
//: Pour regrouper, utilisez les parenthèses `( )` :
(300 + 500 + 400) / 3
//: - experiment:
//:(Expérience) :\
//: Entraînez-vous à utiliser l’aire de jeux comme une calculatrice.
//: Modifiez quelques-unes des expressions existantes et ajoutez les vôtres.
//: Comme vous pouvez le voir, le résultat affiché dans la barre de résultat change à chacune de vos modifications.
