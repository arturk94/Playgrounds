//: #### Problème animalier
//: Imaginez que l’un de vos amis organise un salon animalier et doive savoir combien d’animaux de chaque type les propriétaires vont amener.
//: Votre ami doit également connaître le nombre total d’animaux et de mammifères.
//:
//: Vous voulez lui venir en aide, et vous ne disposez que d’un outil pour cela : un portable équipé d’Xcode Playgrounds. Nous pouvons peut-être aider votre ami.
//:
//: Les connaissances que vous avez acquises jusqu’à présent vous permettent de comptabiliser les animaux avec un commentaire et un nombre, comme suit :

// Nombre de chiens
5

// Nombre de chats
4

// Nombre de tortues
3

//: Vous pouvez ensuite écrire ces nombres et laisser l’aire de jeux effectuer les calculs, comme suit :

// Nombre total d’animaux
5 + 4 + 3

// Nombre total de mammifères
5 + 4

//: - experiment:
//:(Expérience) :
//:alors que la date du salon approche, vous en savez plus sur les animaux participants. Modifiez le code ci-dessus en conséquence.
//: 
//: Par exemple, si une autre personne amène un chien, prenez en compte la modification en faisant passer le `Nombre de chiens` de `5` à `6` et remplacez le `5` par un `6` partout où il apparaît (`Nombre total d’animaux` et `Nombre total de mammifères`).
//: 
//: Si une personne amène un nouveau type d’animal, ajoutez un nouveau commentaire (`// Nombre de hamsters` par exemple) en indiquant le nombre d’animaux correspondant en dessous, puis actualisez les totaux.
//:
//: - Une autre personne amène un chien.
//: - L’une des tortues est malade et ne pourra pas être présente.
//: - Une autre personne amène un chat.
//: - Une personne amène un hamster (un mammifère).
//: - Une personne amène un iguane (pas un mammifère).







