//: ## Identificateurs
//: Dans Swift, un nom s’appelle officiellement un _identificateur_. Vous verrez souvent le terme _identifier_ (identificateur) dans les messages d’erreur. Les programmeurs utilisent souvent le terme informel _name_ (nom). Ce cours emploie les deux termes pour vous habituer à les utiliser.
//:
//: Les noms des constantes débutent par une lettre minuscule. Comme vous le verrez dans les exemples ci-dessous, ils peuvent comporter plusieurs mots collés à la suite. En effet, si les mots étaient séparés, Swift considérerait chacun d’entre eux comme des identificateurs distincts.
//:
//:Pour faciliter la lecture des identificateurs comprenant plusieurs mots, tous les mots, à l’exception du premier, doivent commencer par une majuscule. Cette façon d’écrire s’appelle la _camelCase_, car les lettres capitales au milieu du nom font penser aux bosses d’un chameau. 🐫
let numberOfDogs = 6 + 2

let numberOfCats = 5 - 1

let numberOfTurtles = 2 + 1

let numberOfHamsters = 1 + 1

let numberOfCochonsVietnamiens = 1

let numberOfFishes = 1

let numberOfSnakes = 1

let totalNumberOfAnimals = numberOfDogs + numberOfCats + numberOfTurtles + numberOfHamsters + numberOfCochonsVietnamiens + numberOfFishes + numberOfSnakes

let totalNumberOfMammals = numberOfDogs + numberOfCats + numberOfHamsters + numberOfCochonsVietnamiens

