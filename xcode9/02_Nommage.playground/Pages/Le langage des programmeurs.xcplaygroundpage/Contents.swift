//: ## Les langage des programmeurs
//: Pour développer des apps, il ne suffit pas de savoir quoi écrire dans Xcode. Les programmeurs discutent entre eux pour savoir comment résoudre des problèmes et s’échangent des explications. Il est donc nécessaire de connaître la terminologie qu’ils utilisent.
//:
//: Dans ce cours, vous allez découvrir comment écrire du code et créer des apps, mais vous allez également apprendre les termes dont vous aurez besoin pour parler de votre code avec d’autres développeurs.
//:
//: Par exemple, le terme formel employé pour désigner une ligne de code est _instruction_.
//:
//: Vous avez déjà vu les termes _constante_ et _identificateur_ dans cette aire de jeux. Observez l’instruction ci-dessous pour comprendre d’autres termes.
let numberOfDogs = 101
//: Définir une constante dans Swift correspond à _déclarer_ une constante. L’instruction ci-dessus est donc une _déclaration_. « Je déclare que le nom `numberOfDogs` correspond à la valeur `101`. »
//:
//: Le terme `let` est un exemple de _mot-clé_. Les mots-clés ont une signification précise dans Swift, et vous ne pouvez pas les utiliser en tant que noms. Le mot-clé `let` sert uniquement à déclarer une constante. Vous découvrirez d’autres mots-clés Swift au fur et à mesure.
//:
//: La partie de l’instruction correspondant à `= 101` est l’emplacement où vous _affectez_ une valeur à la constante. On appelle cela l’_affectation_, et le signe égal `=` s’appelle l’opérateur d’affectation.
//:
//: - note: (Remarque) : \
//:Ne cherchez pas tout de suite à retenir tous ces termes, cela viendra petit à petit. Vous verrez ces termes utilisés en contexte tout au long de ce cours, et vous pourrez les retrouver dans le Glossaire du Guide des élèves pour vous rafraîchir la mémoire.
