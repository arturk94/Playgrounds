//: ## Problème animalier
//: Imaginez que l’un de vos amis organise un salon animalier et doive comptabiliser les animaux de chaque espèce censés participer au salon.
//: Votre ami a également besoin de connaître le nombre total d’animaux et de mammifères.
//:
//: Vous voulez lui venir en aide, mais le seul outil dont vous disposez est un portable équipé d’Xcode. Peut-être pouvez-vous utiliser une aire de jeux pour aider votre ami.
//:
//: Les connaissances que vous avez acquises jusqu’à présent vous permettent de comptabiliser les animaux avec un commentaire et un nombre, comme suit :
// Nombre de chiens
7

// Nombre de chats
4

// Nombre de tortues
3

//Nombre de hamsters
9
//: Vous pouvez ensuite copier ces nombres dans de nouvelles lignes de code et laisser l’aire de jeux effectuer les calculs, comme suit :
// Nombre total d’animaux
7 + 4 + 3 + 9

// Nombre total de mammifères
7 + 4 + 9
//: - experiment:
//:(Expérience) :\
//:Alors que la date du salon approche, vous en savez davantage sur les animaux participants.\
//:Modifiez le code ci-dessus pour actualiser les totaux.
//:
//: Par exemple, si une autre personne amène un chien, faites passer le `Nombre de chiens` de `5` à `6` et remplacez le `5` par un `6` partout où il apparaît (`Nombre total d’animaux` et `Nombre total de mammifères`).
//:
//: Si une personne amène un nouveau type d’animal, utilisez le même schéma que pour les autres décomptes d’animaux. Ajoutez un nouveau commentaire (`// Nombre de hamsters` par exemple) sur une ligne, et le nombre correspondant sur la ligne suivante. N’oubliez pas de modifier les totaux.
//:
//: - Une autre personne amène un chien.
//: - L’une des tortues est malade et ne pourra pas être présente.
//: - Une autre personne amène un chat.
//: - Une personne amène un hamster (un mammifère).
//:
//:  Pouvez-vous penser à une manière plus efficace de résoudre ce problème ? Réfléchissez une minute ou deux, puis passez à la suite.
//:
