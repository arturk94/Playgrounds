//: ## Écriture des noms et saisie automatique
//: Xcode conserve tous les noms que vous avez définis. Lorsque vous commencez à écrire un mot, il vous propose donc un menu comportant des suggestions de noms (les noms qu’il connaît). Cela s’appelle la _saisie automatique_.
//:
//: La saisie automatique vous permet d’écrire du code plus rapidement et d’éviter les erreurs de frappe. Plus vous tapez de lettres, plus la saisie automatique vous propose une liste proche de ce que vous recherchez.
//:
//: Lorsque le menu de la saisie automatique apparaît, vous pouvez :
//: - continuer à écrire pour réduire encore le nombre de suggestions ;
//: - utiliser les flèches vers le haut et vers le bas pour vous déplacer dans la liste ;
//: - appuyer sur la touche Retour pour qu’Xcode insère le nom sélectionné.
//:
//: ![Menu contextuel de la saisie automatique](autocomplete.png)
//: La liste comporte des noms que vous n’avez pas définis. Vous pouvez parfaitement les ignorer.
//:
//: L’expérience de la page suivante vous propose de taper des noms pour voir comment fonctionne la saisie automatique.
