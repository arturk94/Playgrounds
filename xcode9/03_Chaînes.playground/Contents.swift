//: ## Chaînes
//: Quelle que soit l’app que vous utiliserez, elle contiendra toujours du texte, qu’il s’agisse d’apps de messagerie, de lecteurs d’actualités ou encore d’apps de réseaux sociaux. Même les apps d’appareil photo en contiennent pour certains de leurs boutons et commandes. En informatique, les valeurs textuelles sont pratiquement toujours appelées des _chaînes_.
//:
//: ### Pourquoi ces valeurs sont-elles appelées des chaînes ?
//: Prenons comme exemple un collier de perles. Les perles peuvent sembler différentes, ou présenter une forme différente, mais elles sont enfilées dans un ordre précis.
//: ![collier de perles](StringOfBeads.png)
//: En règle générale, un caractère seul (la lettre « H », par exemple) n’est pas d’une grande utilité. Un ensemble de caractères permettant de composer des mots, des phrases ou encore des paragraphes est beaucoup plus intéressant. C’est pourquoi, pour toute donnée textuelle, les programmeurs font référence à des _chaînes_.
//: ![chaîne de caractères](StringOfCharacters.png)
