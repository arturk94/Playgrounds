//: ## Caractères
//: En plus des lettres, les données textuelles peuvent bien évidemment inclure des signes de ponctuation, des chiffres, des symboles ou encore des espaces. Ces éléments s’appellent des _caractères_, et les _chaînes_ sont composées de _caractères_.
//:
//: Les lettres sont des caractères :
//: ## Aa Bb Cc Dd Ee
//: De même que les signes de ponctuation :
//: ## ! ? . , ; :
//: Les chiffres :
//: ## 1 2 3 4 5 6 7 8 9 0
//: Les symboles :
//: ## # @ $ & © π * + - /
//: Les caractères invisibles, tels que les espaces et les tabulations, en sont également :\
//: _(Invisibles, ils sont pourtant bien présents.)_
//: ## `                         `
//:
//:
//: Maintenant que vous connaissez les différents composants, il est temps de définir des chaînes.\
