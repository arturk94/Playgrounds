//: ## Chaînes
//: Quelle que soit l’app que vous utiliserez, elle contiendra toujours du texte, qu’il s’agisse d’apps de messagerie, de lecteurs d’actualités, d’apps de réseaux sociaux, etc. Même les apps d’appareil photo en contiennent pour leurs boutons et commandes.
//:
//: Dans la plupart des langages de programmation, Swift compris, les valeurs textuelles sont appelées des _chaînes_.
//:
//: ### Pourquoi ces valeurs sont-elles appelées des chaînes ?
//: Prenons comme exemple un collier de perles. Chaque perle est différente, mais elles sont enfilées dans un ordre précis.
//:
//: ![collier de perles](StringOfBeads.png)
//: En règle générale, une seule lettre (un « H », par exemple) n’est pas utile en soi. Les mots sont composés d’un ensemble (chaîne) de lettres. C’est pourquoi, pour tout texte, les programmeurs font référence à des _chaînes_. N’importe quel type de texte peut être considéré comme une chaîne.
//:
//: ![chaîne de caractères](StringOfCharacters.png)
