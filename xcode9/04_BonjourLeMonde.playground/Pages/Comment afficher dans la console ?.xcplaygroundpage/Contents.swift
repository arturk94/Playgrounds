/*:
 ## Comment afficher dans la console ?
 
 Vous pouvez utiliser la commande `print` pour envoyer tout ce que vous voyez dans la barre de résultat vers la console.
 
 Ouvrez la barre de résultat, observez la console et constatez-le vous-même avec les exemples de l’aire de jeux.
 
 >(Remarque) : \
 >Le `\n` que vous voyez dans la barre de résultat à côté des instructions print signifie que `print` ajoute une nouvelle ligne à la fin de la chaîne. Autrement, la console afficherait tout le contenu sur la même ligne.
 
 Vous pouvez afficher des chaînes :
*/
"Bonjour, le monde !"
print("Bonjour, le monde !")
//: Des nombres et des calculs :
8
print(8)
7 + 11
print(7 + 11)
//: Des expressions de chaîne :
"Avez-vous vu" + "… " + "peu importe."
print("Avez-vous vu" + "… " + "peu importe.")
//: Des constantes :
let authorName = "Beatrix Potter"
print(authorName)
let bookTitle = "Jemima Puddleduck"
print(bookTitle)
/*:
 Le résultat du contenu entre parenthèses apparaîtra dans la console.
 
