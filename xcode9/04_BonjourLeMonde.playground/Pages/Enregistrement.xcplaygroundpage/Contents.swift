 ## Enregistrement
 
 Vous avez sans doute entendu parler du journal de bord, où le capitaine d’un navire (ou d’un vaisseau spatial) consigne chaque jour tous les événements se déroulant à bord. Mais saviez-vous que ce même principe pouvait s’appliquer aux apps ?
 
 Généralement, les programmeurs envoient des messages vers la console pour consigner, ou _enregistrer_, des données sur un programme pendant que celui-ci est exécuté. Le fait d’écrire un message dans la console constitue un _enregistrement_, et les messages sont parfois appelés des messages de journal.
 
 Les programmeurs se servent souvent des messages de journal pour signaler une erreur ou un comportement inattendu. Comme vous pouvez le voir ci-dessous, les messages permettent de transmettre des avertissements et aident à diagnostiquer des problèmes.
 
