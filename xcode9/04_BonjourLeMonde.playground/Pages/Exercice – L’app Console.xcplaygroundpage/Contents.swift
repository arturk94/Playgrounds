 ## Exercice – L’app Console
 
 Quel genre de messages peut-on envoyer à une console ?
 
 Vous pouvez jeter un œil aux messages que des programmeurs ont envoyés sur votre Mac dans Finder > Applications > Utilitaires > Console. Vous y verrez une suite de messages dont le sens n’est pas évident à première vue. Il s’agit de messages privés que des développeurs se sont échangés. Ils utilisent souvent un jargon qui leur est propre et qu’on ne peut comprendre qu’en connaissant parfaitement l’app dont il est question.
 
 Regardez quelques messages défiler, puis quittez l’app Console.
