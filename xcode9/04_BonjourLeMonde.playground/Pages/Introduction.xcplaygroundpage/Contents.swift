 Ces extraits de code sont identiques : ils vont s’exécuter de la même manière et avoir les mêmes effets. Alors, pourquoi ce style vert sur noir ?
 
 Cela remonte aux années 70 et 80, lorsque les écrans d’ordinateur ne pouvaient afficher qu’une seule couleur, généralement du vert sur un écran noir.
 
 De nos jours, certains programmeurs optent pour un arrière-plan noir pour améliorer leur confort visuel (en particulier s’ils travaillent dans une pièce sombre), ou simplement parce qu’ils aiment le style rétro.
 
