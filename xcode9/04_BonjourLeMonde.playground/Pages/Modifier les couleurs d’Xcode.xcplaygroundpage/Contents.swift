 ## Modifier les couleurs d’Xcode

Les programmeurs aiment personnaliser leur environnement de code pour améliorer leur confort de travail. Dans Xcode, vous pouvez apporter des modifications à votre environnement en sélectionnant Xcode > Preferences… dans la barre de menus :

 ![Barre de menus avec menu Xcode ouvert ](xcode-preferences.png)

Dans la fenêtre Preferences, choisissez l’onglet Fonts &amp; Colors (Polices et couleurs) en vous assurant que Source Editor (Éditeur source) est sélectionné :

 ![Volet des préférences Xcode affichant l’onglet « Fonts and Colors » (Polices et couleurs)](xcode-pref-fonts-and-colors.png)


Pour adopter un style totalement rétro, choisissez l’une des trois options avec un arrière-plan noir (Civic, Dusk ou Midnight) :

 ![Volet des préférences Xcode affichant l’onglet « Font and Colors » (Polices et couleurs), option « Dusk » (Crépuscule) sélectionnée](xcode-pref-dusk-color.png)


Cette modification a rendu le code plus difficile à lire ? Pas d’inquiétude : ce sont _vos_ préférences, et vous pouvez donner l’apparence que vous souhaitez à votre code. Cependant, si l’arrière-plan foncé ne vous gêne pas pour lire votre code, gardez-le pour les prochaines pages de cette aire de jeux, car nous allons ajouter d’autres effets rétro.
 
