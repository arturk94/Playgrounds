## Appeler une fonction
 
Vous ne vous en êtes certainement pas rendu compte, mais vous avez déjà tiré profit de l’abstraction et des fonctions lorsque vous avez utilisé `print()` pour écrire « Bonjour, le monde ! » dans la console. Dans Swift, `print()` est une fonction. Lorsque vous l’utilisez, vous _appelez_ la fonction :
*/
print("Bonjour, le monde !")
print(360)
/*:
De la même manière que s’habiller renferme plusieurs tâches distinctes, la fonction `print()` effectue plusieurs actions lorsque vous l’appelez :
- Elle transforme tout ce que vous lui attribuez, y compris les nombres, en une chaîne.
- Elle ajoute un caractère de nouvelle ligne pour que chaque appel de `print()` apparaisse sur une nouvelle ligne.
- Elle fait apparaître la chaîne dans la console.

Ici, vous appelez une fonction précédemment créée par une autre personne. Il n’est pas nécessaire de connaître tous les détails du fonctionnement de `print()` pour l’appeler.

C’est ce qui rend les fonctions si efficaces. Elles permettent de regrouper des étapes précises au sein d’une définition pouvant être réutilisée à volonté.
 
Dans cette aire de jeux, vous allez pouvoir vous entraîner à appeler des fonctions et apprendre à définir vos propres fonctions.
 
