 ## S’habiller
 
 Imaginez qu’un ami vous invite à aller voir un film. Vous vous êtes levé tard, vous répondez donc : « On se voit là-bas. Je m’habille et j’arrive. »
 
 Votre ami comprend la formule « Je m’habille », mais n’en connaît pas les détails, par exemple quel tee-shirt vous allez choisir.
 
 Pensez en détail aux processus mis en œuvre pour vous habiller : choisir la tenue, sortir les vêtements de la commode ou de l’armoire, enfiler chaque vêtement.
 
 Les mots « Je m’habille » paraissent très simples, mais en considérant toutes les actions que cela comporte, on comprend la complexité qui se cache derrière ces mots.
 
 Les mots « s’habiller » nous permettent d’évoquer le processus d’habillage sans considérer tous les détails que cela implique.
 
 Nous nous servons souvent d’expressions qui englobent plusieurs actions. Des phrases comme « aller chez le dentiste », « faire un sandwich » ou « aller prendre un café » décrivent des activités qui seraient laborieuses à raconter si l’on devait les détailler.
 
 L’idée de définir simplement quelque chose de complexe s’appelle l’*abstraction*. L’abstraction est un principe fondamental du développement logiciel. Il aide les programmeurs à ne pas se laisser submerger par trop de détails et de complexité.
 
 Dans cette leçon, vous allez apprendre à définir et à utiliser des _fonctions_, l’une des manières les plus courantes de définir une abstraction en code.
 
- Experiment:
(Expérience) :\
Prenez quelques minutes pour penser à d’autres phrases simples utilisées pour décrire des activités ou des idées beaucoup plus complexes.

 Vous allez maintenant découvrir comment les programmeurs utilisent des phrases simples pour parler d’opérations compliquées.
 
