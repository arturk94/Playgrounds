 ## API
 
 Les développeurs d’apps travaillent systématiquement à partir de code écrit par d’autres. Ici, un certain nombre de fonctions ont déjà été définies pour faire démarrer, bouger et changer de couleur BoogieBot.
 
 L’ensemble des fonctions qui contrôlent BoogieBot est un exemple d’_Application Programming Interface_ (interface de programmation applicative), communément appelée _API_.
 
 Une API est un ensemble de fonctionnalités spécifiques qu’un développeur de logiciels peut utiliser pour accomplir une tâche. Dans notre cas, l’API BoogieBot permet de réaliser un programme de danse de robot.
 
  ### API BoogieBot
 Démarrer BoogieBot :
 - `startBot()`
 
 Faire bouger BoogieBot :
 
 - `leftArmUp()`, `leftArmDown()`, `rightArmUp()`, `rightArmDown()`
 - `leftLegUp()`, `leftLegDown()`, `rightLegUp()`, `rightLegDown()`
 - `shakeItLeft()`, `shakeItRight()`, `shakeItCenter()`
 - `jumpUp()`, `jumpDown()`
 
 Changer la couleur de BoogieBot :
 - `fabulize()`, `defabulize()`
 
 Vous allez ensuite apprendre à assembler ces fonctions pour créer des chorégraphies.
 
