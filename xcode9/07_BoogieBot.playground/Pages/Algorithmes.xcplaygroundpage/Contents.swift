 ## Algorithmes
 
 Au cours de cette leçon, vous avez appris à créer des chorégraphies pour BoogieBot en regroupant des mouvements dans des fonctions. Des informaticiens pourraient dire que vous avez passé cette leçon à développer des _algorithmes_.
 
Lorsque vous développez un algorithme, vous définissez une série d’étapes à réaliser. Évidemment, il ne s’agit pas toujours de pas de danse : n’importe quelle série d’opérations décrite étape par étape au sein d’un ensemble constitue un algorithme.
 
Définir des algorithmes représente une part importante du travail de programmeur. Certains algorithmes mathématiques complexes peuvent par exemple compresser des vidéos pour les mettre en ligne plus facilement. D’autres n’ont rien à voir avec les mathématiques et peuvent, par exemple, servir à vérifier si l’utilisateur d’une app a reçu un nouveau message et à lui envoyer une notification le cas échéant. Il existe aussi, bien entendu, des algorithmes pour faire danser des robots.
 
