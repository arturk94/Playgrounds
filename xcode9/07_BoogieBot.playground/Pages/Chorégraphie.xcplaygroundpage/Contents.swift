/*:
 ## Chorégraphie
 
 Danser est une activité amusante, mais si vous deviez écrire les mouvements un par un, même les danses les plus simples deviendraient longues et répétitives. C’est la raison pour laquelle les mouvements individuels sont regroupés au sein de chorégraphies nommées. 
 
 Pour créer des chorégraphies dans le code, vous devez assembler des fonctions comme suit :
 */
func doTheDisco() {
    leftArmUp()
    shakeItRight()
    leftArmDown()
    shakeItCenter()
}
/*:
 Une fois que vous avez choisi les mouvements à ajouter à votre chorégraphie, appelez la fonction que vous venez de créer pour commencer à faire danser le robot :
 */
startBot()
fabulize()
doTheDisco()
doTheDisco()
doTheDisco()
/*:
 Les développeurs utilisent les fonctions pour regrouper des blocs de code. Comme nous l’avons vu dans l’aire de jeux Fonctions, les fonctions sont utiles à plusieurs titres :
 
 - Les fonctions sont réutilisables, ce qui permet de gagner du temps sur la lecture et l’écriture.
 - Les fonctions forment un ensemble intelligible. Il n’est pas nécessaire d’en connaître toutes les étapes.
 - Si l’on modifie une fonction, les changements s’appliquent partout où cette fonction est utilisée.
 
 La fonction `doTheDisco()` possède tous ces avantages.
 
