/*:
 ## Constantes
 
 Jusqu’à présent, vous avez procédé de la sorte pour nommer une valeur :
 ```
(Exemple) :
 let name = "Johnny"
 ```
 
 Une fois terminé, `name` correspond_ toujours_ à « Johnny ».
*/
