/*:
 Les valeurs immuables s’appellent des **constantes**, car leur valeur est constante. En utilisant `let` pour déclarer une valeur, vous indiquez qu’elle ne changera jamais.
 
 Cela peut se révéler très utile. Vous aurez parfois besoin que des valeurs restent fixes et qu’elles ne puissent être modifiées par accident. Mais des _changements_ pourront parfois s’avérer nécessaires. Vous devrez alors utiliser des **variables**, et non des constantes. 
*/
