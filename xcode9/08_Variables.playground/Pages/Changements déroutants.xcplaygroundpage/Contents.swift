/*:
 ## Changements déroutants
 
 Vous avez peut-être déjà eu ce sentiment de confusion qui intervient à la suite d’un changement de programme.
 
 Imaginez la situation suivante : vous devez rejoindre six amis au cinéma à 20 heures.
 
 Vous recevez un message vous signalant un changement de programme : vous devez vous rendre dans un autre cinéma, pour voir un autre film, et l’heure de rendez-vous est fixée à 21 heures.
 
 Quelques minutes plus tard, vous recevez un nouveau message vous disant que le programme a encore changé. Maintenant, vous devez vous rejoindre chez l’un de vos amis, chez qui vous regarderez un film. Vous devez y être à 18 heures, et vous commanderez des pizzas.
 
 Puis vous recevez un dernier message vous indiquant que le rendez-vous a maintenant lieu chez un autre ami, à 18 h 30.
 
 - experiment:
(Expérience) :\
Tous ces changements de programme réduisent les probabilités que tout le monde arrive au bon endroit à la bonne heure.\
\
En conservant le programme initial, la probabilité d’erreur aurait-elle été réduite ?
 
 À chaque fois que vous faites un changement, vous prenez le risque qu’une personne utilise une information antérieure et se trompe.
 
 La même chose s’applique à la programmation. En déclarant quelque chose comme une constante, vous garantissez la fixité de sa valeur. Il est impossible qu’une autre partie de votre programme se désynchronise ou utilise une valeur obsolète.
 
 Cependant, vous aurez parfois besoin de variables. Un programme en exécution peut connaître des changements. En programmation, les éléments pouvant être modifiés sont _mutables_, d’après le mot _muter_ qui signifie « changer ».
 
 Les éléments ne pouvant pas être modifiés sont _immuables_. La règle tacite consiste à toujours utiliser une constante si l’élément en question n’est pas susceptible de changer, afin de réduire les risques de confusion et d’erreurs.
 
 En parlant d’erreur, nous allons parler d’une erreur courante avec les constantes et les variables dans la leçon qui suit.
 */
