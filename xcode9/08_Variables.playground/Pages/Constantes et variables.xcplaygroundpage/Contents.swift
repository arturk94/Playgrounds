 ## Constantes et variables
 
En programmation, il existe une nette différence entre les noms qui se rapportent toujours à la même valeur et ceux qui se rapportent à une valeur pouvant évoluer.
 
 Les noms donnés dans le monde réel marquent aussi cette différence, mais nous n’y faisons généralement pas attention.
 
 #### Constante
 
 Prenons par exemple « votre date de naissance ». Votre date de naissance est immuable. Quel que soit votre âge, cette date reste identique. Même dans plusieurs centaines d’années, lorsque les gens évoqueront tout ce que vous avez apporté au monde à travers vos apps, votre date de naissance sera toujours la même.
 
 Votre date de naissance est une constante. Sa valeur est fixée à votre naissance et ne change plus jamais. En programmation, votre `birthdate` serait désignée par une constante.
 
 
 #### Variable
 
 La plupart des noms que nous utilisons au quotidien changent de valeur avec le temps.
 
 Pensez par exemple à « votre tee-shirt préféré » ou à un autre vêtement. Lorsque vous étiez petit, vous aviez peut-être déjà un tee-shirt préféré. En grandissant, vous l’avez remplacé, pour des raisons de taille ou de style, et votre tee-shirt préféré n’est maintenant plus le même. Il va sans doute encore changer avec le temps. Vous utilisez toujours le concept de « tee-shirt préféré », mais le tee-shirt correspondant à cette valeur change au fil du temps. En programmation, votre `favoriteShirt` serait une variable.
 
 #### Un peu des deux
 On retrouve un peu des deux dans la plupart des sports collectifs. Il y a la notion d’« équipe à domicile » et de « visiteurs ». Dans votre établissement, l’équipe à domicile est toujours la même. C’est donc une constante. Le nom ou le concept de « visiteurs » reste identique, mais l’équipe change à chaque match. L’équipe en visite est une variable.
 
