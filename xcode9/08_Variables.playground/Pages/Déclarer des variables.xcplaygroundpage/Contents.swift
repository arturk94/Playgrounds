/*:
 ## Déclarer des variables
 
 La déclaration des variables suit pour ainsi dire le même schéma que la déclaration des constantes. Mais au lieu d’utiliser le mot-clé `let`, vous utilisez le mot-clé `var` :
 */
// Déclarer une constante
let placeOfBirth = "New Jersey"

// Déclarer une variable
var currentLocation = "New Jersey"

/*:
 Une fois que vous avez déclaré votre variable, vous pouvez lui affecter une nouvelle valeur :
*/
// Affecter une nouvelle valeur à une variable
currentLocation = "Californie"

