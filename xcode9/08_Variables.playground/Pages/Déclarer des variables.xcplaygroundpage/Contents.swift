/*:
 Les constantes et les variables ont de nombreux points communs :
- Elles suivent toutes les deux les mêmes règles de nommage.
- Elles associent toutes les deux un nom et une valeur attribuée.
- Elles correspondent toutes les deux à un type spécifique.
 
 La différence est la suivante :
 - La valeur d’une constante *ne peut pas* être modifiée une fois qu’elle a été affectée.
 - La valeur d’une variable *peut* être modifiée une fois qu’elle a été affectée.

Vous allez maintenant apprendre à utiliser des variables.
 */
