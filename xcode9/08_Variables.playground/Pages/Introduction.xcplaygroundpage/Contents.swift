 Vous avez également appris à définir un nom et à lui associer une liste d’instructions en déclarant une fonction :
 */
func printGreeting() {
    let greeting = "Bonjour"
    print(greeting)
}
/*:
Vous commencez sans doute à voir une tendance se dessiner. Une grande partie du travail de programmation consiste à inventer des éléments, à les nommer, puis à les appeler par leur nom pour les utiliser.
 
 Jusqu’à maintenant, vous avez utilisé des constantes lorsque vous aviez besoin d’assigner une valeur à un nom. Cependant, nous n’avons toujours pas répondu à une question importante : pourquoi les appelle-t-on des constantes ?
 
 Comme son nom l’indique, le terme « constante » implique une continuité et renvoie à un élément qui reste le même — qui reste constant — au fil du temps.
 
 Lorsque vous affectez une valeur à une constante, vous êtes assuré que celle-ci restera toujours identique. La valeur associée à ce nom reste constante.
 
 Il existe une autre manière d’associer une valeur à un nom dans Swift. Il s’agit des _variables_.
 
 Les variables vous permettent de modifier la valeur lors de l’exécution de votre programme. La valeur associée au nom peut changer — varier — avec le temps.
 
 Cette aire de jeux va vous apprendre à distinguer les variables des constantes, à déclarer des variables et à déterminer quand utiliser une variable et quand utiliser une constante.
 
 Comment ces concepts fonctionnent-ils, et pourquoi est-ce important ?
 
