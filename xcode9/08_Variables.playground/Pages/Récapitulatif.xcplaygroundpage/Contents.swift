 ## Récapitulatif
 
 Cette aire de jeux vous a permis de découvrir des concepts essentiels :
 
 - Les valeurs déclarées avec le mot-clé `let` sont des constantes et ne peuvent plus être modifiées une fois qu’une valeur leur a été attribuée. Ces valeurs sont donc _immuables_.
 - Les valeurs déclarées avec `var` sont des variables. Elles peuvent se voir attribuer une nouvelle valeur au besoin. Ces valeurs sont donc _mutables_.
 - Les valeurs mutables peuvent être utilisées avec elles-mêmes dans une instruction d’affectation : `score = score + 10`.
 - Les opérateurs d’affectation composée permettent de modifier les valeurs mutables : `score += 10`.
 - Utiliser des constantes et des variables correctement dans votre code le rend plus sûr et facilite sa compréhension.
 
