 ## Au-delà de la bibliothèque standard
 
 Les programmeurs peuvent également créer leurs propres types en associant et en complétant les types et les capacités de la bibliothèque standard. Vous apprendrez à faire cela au fur et à mesure que vous progresserez dans ce cours.
 
 - callout(Experiment):
(Expérience) :\
Réutilisez l’un des types que vous avez imaginés dans l’expérience de la page précédente et trouvez à partir de quel type il aurait pu être créé. Par exemple, `TrainingShoe` pourrait utiliser un `Int` pour la pointure, une `String` pour le nom de la marque, une `Date` pour sa date de commercialisation et un autre `Int` pour son prix en euros.


 Les types et les capacités peuvent être regroupés au sein de collections appelées des _frameworks_ ou des _bibliothèques_. Lorsque vous créez une app, vous pouvez vous appuyer sur des frameworks fournis avec Xcode. Le framework le plus important est celui appelé *Foundation*.
 
 Le framework Foundation met à disposition de nombreux types utilisés pour représenter des éléments plus spécifiques que de simples chaînes ou des nombres issus de la bibliothèque standard de Swift. Il propose par exemple des frameworks pour les dates, les distances et les fichiers enregistrés sur un ordinateur.
 
 Ces frameworks supplémentaires ne sont pas ajoutés automatiquement à vos programmes, car il se peut que vous n’en ayez pas besoin. Ce serait comme prendre toutes vos affaires avec vous pour un rapide aller-retour au supermarché.
 
 Vous allez apprendre à ajouter des frameworks à vos programmes sur la page suivante. 
 
