 ## D’où viennent les types ? 
 
 Swift dispose de types intégrés qui constituent les éléments de base de tout programme fonctionnel. Vous avez beaucoup utilisé les `String` et les `Int`, mais il en existe bien d’autres.
 
 Ils font partie de la _bibliothèque standard de Swift_. Tous les langages de programmation ont une chose en commun : un ensemble de capacités nécessaires à la réalisation de tâches de programmation basiques.
 
 Vous avez beaucoup utilisé les types `String` et les `Int`, mais il en existe bien d’autres. Voici quelques types supplémentaires dont dispose la bibliothèque standard de Swift. Il n’est pas nécessaire de connaître leur fonction pour le moment, même si vous pouvez essayer de la deviner :
 
 - `Array`
 - `Dictionary`
 - `Set`
 - `Sequence`
 - `Error`
 - `Bool`
 
 Vous remarquerez que les noms des types commencent par une majuscule et que la première lettre de chaque mot est également en majuscule. Cela diffère légèrement des règles de nommage des constantes, des variables et des fonctions, qui commencent toutes par des lettres minuscules.
 
 Cette différenciation est voulue. Les noms de types commençant par une majuscule, il est plus facile de les différencier des noms de fonctions, de variables et de constantes. Les deux utilisent la même casse, mais les types utilisent la _casse UpperCamelCase_, tandis que les fonctions, les variables et les constantes utilisent la _casse lowerCamelCase_.
 
