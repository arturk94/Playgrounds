 ## Types importants
 
 Au quotidien, il est important d’utiliser le type d’objet adéquat.
 
 Si vous jouez au tennis, vous devez utiliser une balle et pas autre chose, comme un œuf par exemple.
 
 À l’inverse, une douzaine de balles de tennis ne vous servira pas à grand-chose pour faire une omelette.
 
 Différents types d’objets ont des propriétés et des comportements qui leur sont propres. La balle de tennis est remplie d’air et rebondit si vous la faites tomber. L’œuf est rempli de blanc et de jaune d’œuf et se casse si vous le faites tomber.
  
 En programmation, les types suivent cette logique.
 
 Par exemple, vous avez déjà utilisé le type appelé chaîne pour travailler avec des caractères et du texte. Les chaînes ont des propriétés et des comportements définis pour servir cet objectif.
 
