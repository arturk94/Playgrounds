 ## Récapitulatif
 
 Toutes les constantes et les variables que vous utilisez dans Swift possèdent un type.
 
 La valeur des variables peut changer, mais pas leur type.
 
 Les types décrivent :
 - à quoi correspond une valeur ;
 - ce qu’une valeur peut faire ;
 - où vous pouvez utiliser une valeur.
 
 Swift vérifie chaque ligne de code que vous écrivez pour s’assurer que vous utilisez les types correctement.
 
 Swift peut déduire le type d’une constante ou d’une variable d’après la valeur qui lui a été affectée.
 
 Vous pouvez utiliser les annotations de type pour spécifier clairement le type d’une constante ou d’une variable.
 
