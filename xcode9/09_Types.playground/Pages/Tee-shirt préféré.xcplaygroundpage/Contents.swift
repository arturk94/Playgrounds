 ## Tee-shirt préféré
 
 Lorsqu’on vous demande de penser à votre tee-shirt préféré, vous pensez à un tee-shirt et pas à une banane. Ce ne serait pas logique. Une banane n’est pas un tee-shirt.
 
 À l’inverse, si vous pensez à votre fruit préféré, qu’il s’agisse de la banane, de la pomme ou d’autre chose, il est certain qu’il s’agira d’une sorte de fruit.
 
 Lorsque quelqu’un nomme quelque chose — son « fruit préféré » par exemple —, ce nom correspond généralement à un type d’objet précis.
 
 L’idée de « fruit préféré » n’a de sens que si elle est liée à un genre de fruit. Si elle était liée à « clé anglaise » ou à « astronomie », cela n’aurait aucun sens. Votre cerveau cherche naturellement à vérifier que les noms correspondent au bon type d’objet.
 
 De la même manière, Swift mémorise les types des valeurs associées aux constantes et aux variables, et vérifie que vous n’attribuez pas par mégarde la valeur banane alors que la valeur attendue est un tee-shirt.
 
