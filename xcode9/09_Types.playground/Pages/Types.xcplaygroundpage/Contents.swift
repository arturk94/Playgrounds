/*:
 ## Types
 Dans Swift, toutes les valeurs correspondent à un _type_.
 
 Tout comme les types d’objets que vous croisez dans la vie quotidienne, les types dans Swift possèdent des propriétés et des comportements qui leur sont propres.

 Vous avez déjà travaillé avec deux types de valeurs :
 
 - Les chaînes comme `"Bonjour"` et `"Wimbledon"`
 - Les nombres comme `101` et `-10`
 */
let string = "Je suis une chaîne"
let number = 12
//: Vous avez aussi vu que ces types n’avaient pas le même comportement. Que se passe-t-il si, par exemple, vous les ajoutez ?
1 + 1 // égale 2
"1" + "1" // égale "11"
/*:
 Dans Swift, le nom du type correspondant aux chaînes est `String` (« chaîne » en anglais).
 
 Le nom du type correspondant aux nombres est `Int`, abréviation d’« integer » (« entier » en anglais). Une valeur `Int` est un nombre entier sans partie fractionnaire. Un `Int` peut être un nombre positif, un nombre négatif ou zéro.
 
 Vous connaissez pour l’instant deux types, mais il vous en reste beaucoup d’autres à découvrir. Apprendre à utiliser les différents types et à en créer vous-même de nouveaux sont deux aspects essentiels de la programmation.
 
 Imaginez une boîte à outils comportant une clé à douille, un tournevis, une pince et un marteau. Chacun de ces outils est utile pour des tâches spécifiques, et totalement inutile pour d’autres. À chaque fois que vous apprenez à utiliser un nouveau type dans Swift, vous ajoutez un nouvel outil à votre boîte à outils de programmation.
 
