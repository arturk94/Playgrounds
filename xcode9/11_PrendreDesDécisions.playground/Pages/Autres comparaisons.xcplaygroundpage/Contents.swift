/*:
 ## Autres comparaisons
 
 L’opérateur de comparaison `==` est très utile, mais vous allez vite vous lasser de vérifier si tel ou tel nombre est égal à un autre. Nous allons donc découvrir d’autres types de comparaisons de nombres :
 */
// Inférieur à
1 < 2
2 < 2
3 < 2

// Supérieur à
1 > 2
2 > 2
3 > 2
/*:
 Certains opérateurs de comparaison sont composés de deux symboles qui, mis côte à côte, ont un sens spécifique :
 */
// Différent
1 != 2
2 != 2
2 != 1

// Inférieur ou égal à
2 <= 2
1 <= 2
3 <= 2

// Supérieur ou égal à
1 >= 2
2 >= 2
3 >= 2

