/*:
 ## Exercice – Mise en pratique des instructions else
 
 Lorsque le résultat d’une instruction `if` est faux, les instructions `else` retiennent le code à exécuter. Elles peuvent le simplifier et permettent de mieux comprendre le cheminement que doit suivre le programme.
*/
let secretWord = "banane"
let guess = "pomme"

if secretWord == guess {
    print("Vous avez vu juste.")
}
if secretWord != guess {
    print("Vous n’y êtes pas du tout.")
}
