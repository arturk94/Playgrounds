/*:
 ## Vrai et faux
 
 Le jeu des 20 questions consiste à faire deviner quelque chose en posant des questions auxquelles on peut répondre par *oui* ou *non*. Les questions fermées peuvent se révéler très efficaces.
 
 Le code que vous écrivez peut prendre la forme d’une question à laquelle on peut répondre par _true_ (vrai) ou _false_ (faux). Les résultats apparaissent dans la barre de résultat, comme pour les calculs.
 
 Les valeurs `true` et `false` ont un rôle bien précis dans Swift, comme vous pouvez le voir ici :
*/
true
false
//: Vous pouvez les attribuer à des constantes, comme des nombres ou des chaînes :
let oui = true
oui
let non = false
non
//: Les valeurs `true` et `false` sont des _valeurs booléennes_. Elles tiennent leur nom de George Boole, un mathématicien du XIXᵉ siècle qui a réalisé l’importance de poser des questions claires avec des réponses simples. Le type `Bool` sert à représenter les valeurs booléennes dans Swift. « true » et « false » sont les _seules_ valeurs `Bool` possibles. La ligne de code suivante renverrait une erreur :
//let perhaps = maybe
//: Si `true` et `false` sont les réponses, comment doit-on poser la question ?
