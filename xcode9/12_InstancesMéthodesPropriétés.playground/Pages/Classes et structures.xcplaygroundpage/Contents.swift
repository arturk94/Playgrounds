 Concevoir des apps dans Swift vous amènera à travailler avec des instances de _structs_ (abréviation de _structures_) et de _classes_. Elles permettent toutes deux de définir des types dans Swift.
 
 Pour le moment, il vous suffit de retenir que les structs et les classes ont de nombreux points communs :
 
- Elles possèdent toutes les deux des instances.
- Leurs instances sont créées à partir d’un initialiseur.
- Elles peuvent être associées à des méthodes.
- Elles peuvent être associées à des propriétés.
 
Pour créer et utiliser des instances, vous devrez saisir le même code Swift, qu’il s’agisse d’un type struct ou class.
 
Pendant le reste du cours, vous allez vous entraîner à créer des instances et à utiliser des méthodes et des propriétés. Vous apprendrez même à définir vos propres structures. Ce cours n’a pas pour objectif d’énumérer les différences entre les classes et les structures, mais vous pourrez les découvrir plus tard, lorsque vous serez plus aguerri.

