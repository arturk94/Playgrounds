 ## Instances
 
 Vous avez déjà appris à coder avec plusieurs types, dont `String`. Chaque valeur individuelle de `String`, par exemple "Hello", est une _instance_ de ce type.
 
 - `String` est un type.
 - `"Hello"` est une _instance_ de `String`.
 
Sans le savoir, vous avez déjà créé des instances de types `Int` et `String`. En effet, chaque valeur que vous créez dans un programme est une instance du type qui lui est associé.
 
Les constantes `hello` et `aDifferentHello` sont deux instances de `String` qui se trouvent posséder les mêmes données.

