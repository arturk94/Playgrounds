 ## Qu’est-ce qu’une instance ?
 
Vous avez découvert ce qu’étaient les types dans Swift et leurs similitudes avec les catégories que l’on peut trouver dans la vie de tous les jours.
 
Les types que l’on trouve dans notre vie quotidienne comportent beaucoup d’instances physiques :
 
- Le type « Voiture » compte un nombre astronomique de variations individuelles, chaque voiture ayant un kilométrage, une marque, un modèle, une couleur, un niveau de carburant, des rayures et des bosselures qui lui sont propres. Chaque voiture possède aussi des comportements spécifiques : la manière dont elle démarre, freine, change de vitesse, etc.
 
 
- Quant au type « Ville », chaque instance a un nom, un nombre d’habitants, une position géographique et des monuments différents. De plus, les villes appliquent différentes méthodes de gestion des situations d’urgence ou de collecte des déchets.
 
Le type définit ou décrit les propriétés et les comportements d’une catégorie d’objets spécifique. Mais les objets appartenant à cette catégorie (chaque voiture, chaque ville) sont tous des instances distinctes et indépendantes de ce type.
 
Prenons par exemple deux voitures de la même marque, du même modèle, fabriquées la même année. L’une fonce sur l’autoroute tandis que l’autre est en panne sèche sur le bas-côté. Le type est le même, mais les instances sont différentes.
 
Maintenant, imaginez deux villes. Ces deux villes ne sont pas situées au même endroit. Un monument d’une des villes attire beaucoup de touristes, tandis qu’un monument de l’autre est délabré et attire peu de visiteurs. Les deux instances sont des villes, mais elles peuvent être très différentes l’une de l’autre.
 
En programmation, vous pouvez créer et utiliser plusieurs instances d’un même type. Chaque instance possède un ensemble de valeurs qui lui est propre, et un comportement distinct des autres instances.
 
Nous allons maintenant voir des exemples d’instance en programmation.
 
