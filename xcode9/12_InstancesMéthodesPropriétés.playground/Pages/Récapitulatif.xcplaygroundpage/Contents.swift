 ## Récapitulatif
 
 Dans cette leçon, vous avez appris que chaque valeur d’un type spécifique s’appelle une instance.
 
 Vous avez également appris que les types n’étaient pas de simples étiquettes comme `String` ou `Int`, mais qu’ils avaient tous des capacités particulières.
 
 Les types peuvent être pourvus de propriétés, qui attribuent à chaque instance un ensemble de valeurs qui lui est propre.
 
 Les types peuvent être associés à des méthodes d’instance, qui sont des fonctions définies à l’intérieur d’un type.
 
 Consultez la documentation fournie avec Xcode pour connaître les méthodes et les propriétés disponibles. Vous pouvez aussi accéder à la documentation en cliquant sur le code et en maintenant la touche Option enfoncée, ou un utilisant l’inspecteur Aide rapide. Enfin, vous pouvez également ouvrir le lecteur de documentation dans une nouvelle fenêtre pour effectuer vos recherches.
 
 Vous avez vu qu’un type en apparence très simple, comme `String`, disposait d’un grand nombre de méthodes et de propriétés.
 
 Vous avez appris pourquoi il était utile d’ajouter des éléments tels que des méthodes d’instance et des propriétés au lieu de définir des fonctions d’ordre supérieur.
 
