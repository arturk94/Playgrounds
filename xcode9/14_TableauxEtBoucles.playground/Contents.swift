 ## Listes
 
 Lorsque vous avez étudié les chaînes, vous avez fait des exercices où il était question de listes d’éléments. Vous avez créé ces listes en associant plusieurs chaînes à l’aide du caractère de nouvelle ligne `\n`. 
 
 ```
(Exemple) :
 let shoppingList = "Œufs" + "\n" + "Tomates" + "\n" + ...
 ```
 
 Cela vous donne une liste bien organisée, mais vous ne pouvez pas en faire grand-chose de plus. Pensez aux questions auxquelles une liste vous permettrait de répondre :
 
 - Quel est le premier élément ? Quel est le dernier élément ? Quel est le 24ᵉ élément ? 
 - Combien d’éléments y a-t-il ?
 - Comment faire pour réorganiser la liste ?
 - Comment faire si ma liste ne comporte pas de valeurs `String`, mais d’autres types de valeurs ?
 
 Il existe un type spécial appelé **Array** (tableau), capable d’accueillir des listes d’éléments. Dans cette aire de jeux, vous allez apprendre à créer des tableaux et à les utiliser pour travailler.  
