 ## Listes
 
 Tout le monde utilise des listes. Vous avez sans doute une liste de choses à faire, une liste de souhaits, une playlist iTunes, voire une liste de rêves à réaliser. Les listes sont également très prisées en programmation.
 
 Lorsque vous avez découvert les chaînes, certains exercices ont nécessité que vous fassiez des listes d’éléments. Vous avez alors créé des listes en associant plusieurs chaînes et en les séparant avec le caractère de nouvelle ligne `\n`.
 
 ```
(Exemple) :
 let shoppingList = "Œufs" + "\n" + "Tomates" + "\n" + ...
 ```
 
 Avec ce code, la liste qui s’affiche à l’écran est bien organisée, mais vous ne pouvez pas en faire grand-chose de plus. Imaginez ce que les listes vous permettraient de faire :

 - Comment appeler une fonction sur tous les membres de la liste sans avoir à tous les récrire ?
 - Comment vérifier qu’un élément a bien été ajouté à une liste ?
 - Si votre liste comporte plusieurs centaines d’éléments, comment facilement supprimer l’élément « Tomates » ?
 - Que se passe-t-il si votre liste n’est pas composée de valeurs `String`, mais par exemple d’une liste de prix que vous souhaitez additionner ?
 - Quel est le premier élément ? Quel est le dernier élément ? Quel est le 24ᵉ élément ?
 - Combien d’éléments y a-t-il ?
 - Comment faire pour réorganiser la liste ?

 Dans Swift, les listes s’appellent des _tableaux_.
 
  Pensez à des apps iOS dans lesquelles vous devez faire défiler une liste d’éléments. Ces apps utilisent très probablement des tableaux.
  
 Dans cette leçon, vous allez apprendre à créer des tableaux et à les utiliser pour travailler.

