 ## Récapitulatif
 
 Vous pouvez utiliser des tableaux pour organiser une liste d’éléments. Les tableaux ont deux caractéristiques principales :
 
 - Les éléments d’un tableau appartiennent tous au même type.
 - Les éléments d’un tableau sont classés dans un ordre spécifique.
 
 Grâce à ces deux caractéristiques, vous pouvez utiliser l’index pour accéder à des éléments situés à des emplacements spécifiques du tableau, et vous récupérerez toujours une valeur d’un type connu.
 
 Voici d’autres connaissances que vous avez acquises dans ce chapitre :
 
 - Le premier index d’un tableau est 0, et non 1.
 - Accéder à des tableaux à partir de l’index peut se révéler dangereux. Si l’index ne figure pas dans le tableau, votre programme plantera.
 - Vous pouvez utiliser la propriété `count` pour connaître le nombre d’éléments que comporte un tableau.
 - Vous pouvez utiliser des boucles `for…in` pour accéder aux éléments dans l’ordre et sans aucun risque, même si vous ne connaissez pas le nombre d’éléments que contient le tableau.
 - Avec les tableaux mutables, vous pouvez ajouter, supprimer et remplacer des éléments.
 
 Mettez vos nouvelles connaissances en pratique grâce aux exercices qui suivent.

