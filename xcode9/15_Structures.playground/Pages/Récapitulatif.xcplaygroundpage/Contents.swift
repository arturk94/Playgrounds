 ## Récapitulatif
 
Toutes les apps ont un modèle qui représente les données qu’elles utilisent.
 
Swift vous permet de définir vos propres types en organisant les données de votre app de manière cohérente.
 
Pour créer des types personnalisés, vous pouvez notamment définir des structures.

Une structure est un type pouvant regrouper des propriétés et des méthodes.
 
Certaines propriétés contiennent des données, comme des variables ou des constantes. D’autres retournent une valeur calculée.

Créer et utiliser des instances de vos types personnalisés revient au même que pour les types intégrés à Swift.
 
Vous pouvez utiliser vos types personnalisés comme n’importe quel autre type, et leur donner des rôles de paramètres de fonction ou de types de retour.

Vous allez à présent concevoir et construire vos propres structures.

