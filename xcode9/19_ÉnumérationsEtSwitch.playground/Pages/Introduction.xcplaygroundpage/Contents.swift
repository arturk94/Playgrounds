 ## Choix multiples
 
 La vie nous met souvent face à des choix. Le plus souvent, il faut choisir parmi plusieurs possibilités, et l’abstention n’est pas une option.
 
 Par exemple, à la cafétéria de l’école, on peut avoir le choix au déjeuner entre des pâtes, un hamburger et de la soupe. Le personnel de la cafétéria n’a pas le temps de personnaliser les plats, vous devez donc choisir parmi ces trois options.
 
 Choisissez un plat. 
 
 Ce système fonctionne très bien pour la cafétéria et pour l’école :
 
 - Le personnel sait qu’il lui suffit de distribuer l’un des trois repas.
 - Le personnel peut préparer et servir chaque plat rapidement et efficacement.
 - Il ne perd pas de temps à comprendre et à noter des commandes spéciales.
 - L’établissement sait que tous les élèves ont bénéficié d’un repas nutritif et seront en forme pour les cours de l’après-midi.
 
 En tant que programmeur, vous pouvez tirer avantage de l’utilisation de ce genre de choix préétablis dans vos programmes. 

