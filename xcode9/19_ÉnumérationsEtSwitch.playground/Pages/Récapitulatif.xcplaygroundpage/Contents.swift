 ## Récapitulatif
 
 Les énumérations permettent de représenter un élément parmi un groupe de valeurs associées. Toutes les valeurs possibles sont des cas.
 
 Créer une enum revient à créer un nouveau type. Les valeurs des instances de ce type doivent impérativement correspondre à l’un des cas spécifiés.
 
 L’utilisation d’enum facilite l’écriture et la compréhension du code, car les valeurs et leur signification sont toujours clairement déterminées.
 
 Vous pouvez comparer les valeurs d’une enum avec `==`, ou utiliser une instruction switch pour tester toutes les valeurs possibles.
 
 De la même manière que les instructions if, les instructions switch permettent de prendre des décisions dans votre code. Les instructions switch fonctionnent parfaitement avec les enum, mais elles peuvent être utilisées avec tout type de valeur.
 
 Comme les instructions switch doivent être exhaustives, toutes les valeurs possibles doivent être prises en compte. Vous pouvez utiliser un cas par défaut (default) pour traiter des valeurs n’ayant pas été spécifiées. 
 
 Les enum peuvent accueillir des propriétés calculées et des méthodes, comme les structures.
 
